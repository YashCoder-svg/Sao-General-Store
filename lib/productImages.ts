export function getProductImageByName(name?: string | null): string {
  if (!name) return PRODUCT_PLACEHOLDER_IMAGE;

  // Normalize name for better matching (remove extra spaces, handle dashes)
  const normalizedSearch = name.toLowerCase().replace(/[\u2013\u2014]/g, "-").trim();

  const key = Object.keys(productImages).find((k) => {
    const normalizedKey = k.toLowerCase().replace(/[\u2013\u2014]/g, "-").trim();
    return normalizedKey === normalizedSearch;
  });

  return key ? productImages[key] : PRODUCT_PLACEHOLDER_IMAGE;
}
